public class Calc {
    public static Integer sum(int x, int y){
        return x+y;
    }
    public static Integer diff(int x, int y){
        return x-y;
    }
    public static Integer prod(int x, int y){
        return x*y;
    }
    public static double quot(double x, double y){
        return x/y;
    }
}
